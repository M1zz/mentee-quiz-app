function ThreeChoiceSelector({ leftLabel, rightLabel, leftDesc, rightDesc, value, onChange, disabled }: {
  leftLabel: string; rightLabel: string; leftDesc?: string; rightDesc?: string;
  value: -1 | 0 | 1 | null; onChange: (v: -1 | 0 | 1) => void; disabled: boolean;
}) {
  const choices: { value: -1 | 0 | 1; label: string; desc?: string }[] = [
    { value: -1, label: leftLabel, desc: leftDesc },
    { value: 0, label: '상황에 따라 다름', desc: '둘 다 해당하거나 중간' },
    { value: 1, label: rightLabel, desc: rightDesc },
  ];

  return (
    <div style={{ display: 'flex', flexDirection: 'column', gap: 10 }}>
      {choices.map((choice, idx) => {
        const isSelected = value === choice.value;
        const isMiddle = choice.value === 0;
        return (
          <button key={idx} onClick={() => !disabled && onChange(choice.value)} disabled={disabled}
            style={{
              padding: isMiddle ? '14px 20px' : '18px 20px', fontSize: isMiddle ? 13 : 14, fontWeight: 500,
              color: isSelected ? '#fff' : isMiddle ? C.textMute : C.textDim,
              background: isSelected ? (isMiddle ? '#2a2a4a' : `linear-gradient(135deg, ${C.accent}, #7c3aed)`) : (isMiddle ? '#0d0d1a' : C.dark),
              border: `1.5px solid ${isSelected ? (isMiddle ? '#4a4a6a' : C.accent) : (isMiddle ? '#1a1a35' : '#2a2a4a')}`,
              borderRadius: 14, cursor: disabled ? 'default' : 'pointer', textAlign: 'left', lineHeight: 1.5,
              transition: 'all 0.25s ease', opacity: disabled ? 0.6 : 1,
            }}>
            <div>{choice.label}</div>
            {choice.desc && <div style={{ fontSize: 11, marginTop: 4, opacity: 0.7, fontWeight: 400 }}>{choice.desc}</div>}
          </button>
        );
      })}
    </div>
  );
}

function RadarChart({ scores }: { scores: number[] }) {
  const size = 240; const center = size / 2; const maxR = 90;
  const labels = ['학습방식', '진행속도', '동기부여', '문제해결', '성장방향'];
  const angles = labels.map((_, i) => (Math.PI * 2 * i) / 5 - Math.PI / 2);
  const normalized = scores.map(s => Math.max(0.15, (s + 3) / 6));
  const points = normalized.map((s, i) => ({ x: center + Math.cos(angles[i]) * maxR * s, y: center + Math.sin(angles[i]) * maxR * s }));
  const pathD = points.map((p, i) => `${i === 0 ? 'M' : 'L'} ${p.x} ${p.y}`).join(' ') + ' Z';
  return (
    <svg width={size} height={size} viewBox={`0 0 ${size} ${size}`}>
      {[0.33, 0.66, 1].map((level, li) => (
        <polygon key={li} points={angles.map(a => `${center + Math.cos(a) * maxR * level},${center + Math.sin(a) * maxR * level}`).join(' ')} fill="none" stroke="#1e1e3e" strokeWidth={1} />
      ))}
      {angles.map((a, i) => (
        <g key={i}>
          <line x1={center} y1={center} x2={center + Math.cos(a) * maxR} y2={center + Math.sin(a) * maxR} stroke="#1e1e3e" strokeWidth={1} />
          <text x={center + Math.cos(a) * (maxR + 22)} y={center + Math.sin(a) * (maxR + 22)} textAnchor="middle" dominantBaseline="middle" fill={C.textDim} fontSize={10} fontFamily="'Pretendard', sans-serif">{labels[i]}</text>
        </g>
      ))}
      <path d={pathD} fill="rgba(99,102,241,0.18)" stroke={C.accent} strokeWidth={2.5} />
      {points.map((p, i) => (<circle key={i} cx={p.x} cy={p.y} r={4.5} fill={C.accent} stroke="#fff" strokeWidth={1.5} />))}
    </svg>
  );
}

