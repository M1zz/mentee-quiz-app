function getTypeCode(scores: number[]): string {
  let code = '';
  code += scores[0] <= 0 ? 'T' : 'P';
  code += scores[1] <= 0 ? 'F' : 'S';
  code += scores[2] <= 0 ? 'I' : 'E';
  code += scores[3] <= 0 ? 'A' : 'C';
  code += scores[4] <= 0 ? 'D' : 'W';
  return code;
}

